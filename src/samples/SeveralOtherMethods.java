package samples;

public class SeveralOtherMethods {

    public int methodA(){
        return 5 * 6 + 7;
    }

    public int methodB(){
        return 7 * 6 + 5;
    }

    public int methodC(int a, int b){
        return a * 6 + b;
    }

    public int methodD(int a, int b, int c){
        return a * c + b;
    }
}
